    public void Show_Users_In_JTable()
   {
        ArrayList<Car> list = getUsersList();
       DefaultTableModel model = (DefaultTableModel) carTable.getModel();
       Object[] row = new Object[7];
       for(int i = 0; i < list.size(); i++)
       {
           row[0] = list.get(i).getCar_id();
           row[1] = list.get(i).getClientName();
           row[2] = list.get(i).getCompany_name();
           row[3] = list.get(i).getModel_name();
           row[4] = list.get(i).getYear_made();
           row[5] = list.get(i).getPrice();
           row[6] = list.get(i).getCondition();
           model.addRow(row);
       }
    }
    
    public void executeSQlQuery(String query, String message, int messageFlag)
   {
       Connection con = conn;
       Statement st;
       try{
           st = con.createStatement();
           if((st.executeUpdate(query)) == 1)
           {
               // refresh jtable data
               DefaultTableModel model = (DefaultTableModel)carTable.getModel();
               model.setRowCount(0);
               Show_Users_In_JTable();
               if(messageFlag == 1) {
                   JOptionPane.showMessageDialog(null, "Data "+message+" Succefully");
               }
           }else{
               JOptionPane.showMessageDialog(null, "Data Not "+message);
           }
       }catch(Exception ex){
           ex.printStackTrace();
       }
   }
