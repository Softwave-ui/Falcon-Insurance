    private void jTable_DisplayLicenseMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_DisplayLicenseMousePressed
        // TODO add your handling code here:
        int i = jTable_DisplayLicense.getSelectedRow();

        TableModel model = jTable_DisplayLicense.getModel();
        
         // Display Slected Row In JTexteFields
        id.setText(model.getValueAt(i,0).toString());
        clientName.removeAllItems();
        String name = model.getValueAt(i,1).toString();
        clientName.addItem(name);
        clientNameList = getClientData();
        for (int j = 0; j < clientNameList.size(); j++) {
            String x = clientNameList.get(j).getClient_name();
            if(!(x.equals(name))) {
                clientName.addItem(x);
            }   
        }        
        skills.setText(model.getValueAt(i,2).toString());
        
        issue.setText(model.getValueAt(i,3).toString());
        
        exp.setText(model.getValueAt(i,4).toString());
        
    }//GEN-LAST:event_jTable_DisplayLicenseMousePressed

