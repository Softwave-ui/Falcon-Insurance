    public void Show_Users_In_JTable()
   {
        ArrayList<DrivingLicense> list = getUsersList();
       DefaultTableModel model = (DefaultTableModel) jTable_DisplayLicense.getModel();
       Object[] row = new Object[6];
       for(int i = 0; i < list.size(); i++)
       {
           row[0] = list.get(i).getLicense_id();
           row[1] = list.get(i).getClientName();
           row[2] = list.get(i).getDriving_skills();
           row[3] = list.get(i).getIssue_date();
           row[4] = list.get(i).getExpiry_date();
           model.addRow(row);
       }
    }
    
    public void executeSQlQuery(String query, String message, int messageFlag)
   {
       Connection con = conn;
       Statement st;
       try{
           st = con.createStatement();
           if((st.executeUpdate(query)) == 1)
           {
               // refresh jtable data
               DefaultTableModel model = (DefaultTableModel)jTable_DisplayLicense.getModel();
               model.setRowCount(0);
               Show_Users_In_JTable();
               if(messageFlag == 1) {
                   JOptionPane.showMessageDialog(null, "Data "+message+" Succefully");
               }
           }else{
               JOptionPane.showMessageDialog(null, "Data Not "+message);
           }
       }catch(Exception ex){
           ex.printStackTrace();
       }
   }
